    }
    
    static var primaryNavy: UIColor {
        return UIColor(red: 30/255, green: 64/255, blue: 132/255, alpha: 1.0)
    }
    
    static var blueLinear: UIColor {
        return UIColor(red: 146/255, green: 163/255, blue: 253/255, alpha: 1.0)
    }
}
